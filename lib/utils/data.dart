class Voice {
  late String name;
  late String displayName;
  late String localName;
  late String shortName;
  late String gender;
  late String locale;
  late String localeName;
  late String sampleRateHertz;
  late String voiceType;
  late String status;

  Voice(
      {required this.name,
      required this.displayName,
      required this.localName,
      required this.shortName,
      required this.gender,
      required this.locale,
      required this.localeName,
      required this.sampleRateHertz,
      required this.status,
      required this.voiceType});

  Map<String, dynamic> toJson() => {
        'Name': name,
        'DisplayName': displayName,
        'LocalName': localName,
        'ShortName': shortName,
        'Gender': gender,
        'Locale': locale,
        'LocaleName': localeName,
        'SampleRateHertz': sampleRateHertz,
        'VoiceType': voiceType,
        'Status': status
      };

  Voice.fromJson(Map<String, dynamic> json) {
    name = json['Name'];
    displayName = json['DisplayName'];
    localName = json['LocalName'];
    shortName = json['ShortName'];
    gender = json['Gender'];
    locale = json['Locale'];
    localeName = json['LocaleName'];
    sampleRateHertz = json['SampleRateHertz'];
    voiceType = json['VoiceType'];
    status = json['Status'];
  }

  @override
  bool operator ==(Object other) => other is Voice && other.name == name;

  @override
  int get hashCode => name.hashCode;
}

Future<void> readConfig() async {
  var configString = await DataBus.configFile.readAsString();
  DataBus.fromJson(jsonDecode(configString));
}

Future<void> writeConfig() async {
  var toWrite = jsonEncode(DataBus.toJson());
  await DataBus.configFile.writeAsString(toWrite);
}
